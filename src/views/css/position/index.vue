        <h1>position总结</h1>
        <pre>
            1. static: 元素在文档常规流中的当前位置，此时top,left,bottom,right，z-index不生效

            2. relative：相对定位，相对于自身的位置进行定位,可设置top,left,bottom,right，z-index

            3. absolute: 绝对定位，脱离了文档流，相对于具有position，并且值为非static属性的祖先元素
                         进行定位，可以设置外边距，并且和其他元素不重叠 可设置top,left,bottom,right，z-index

            4. fixed:  浮动定位，相对于具有transform, perspective,或filter的非none属性的祖先元素进行定位， 
                       可设置top,left,bottom,right，z-index

            5. sticky:  粘性定位，元素根据正常的文档流进行定位，但是相对于具有滚动属性的块级祖先元素进行top,left,
                        bottom,right偏移(注意：如果当前元素和具有滚动属性的块级祖先级元素存在top,left,bottom,
                        right间距，并且这个间距大于当前元素对应的top或者left或者right或bottom的值，那么当前元素
                        位置不动，如果小于，那么当前元素的偏移位置等于它的top或left或right或bottom减去对应的间距)。
                        滚动的时候它会固定到具有滚动属性的祖先元素上,并且进行top,left,right,bottom偏移 
        </pre>
